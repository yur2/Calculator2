   
class Calculator{ 
  
 
 public static void add(int a, int b) {
  System.out.println(a+"+"+b+"="+(a+b));
 }
 
 public static void minus(int a, int b) {
  System.out.println(a+"-"+b+"="+(a-b));
 }
 
 public static void multiple(int a, int b) {
  System.out.println(a+"*"+b+"="+(a*b));
 }
 
 public static void devide(int a, int b) {
  System.out.println(a+"/"+b+"="+(a/b));
 
  
}
 }